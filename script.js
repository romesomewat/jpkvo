function getText(path, fallback = "") {
  return path.split(".").reduce((obj, key) => obj && obj[key] !== undefined ? obj[key] : undefined, content) ?? fallback;
}

function template(str, values) {
  return String(str || "").replace(/\{(\w+)\}/g, (_, key) => values[key] ?? "");
}

function formatMoney(value) {
  return new Intl.NumberFormat("vi-VN").format(Math.round(Number(value) || 0)) + " " + getText("productCard.priceCurrency", "₫");
}

function normalizeText(value) {
  return String(value || "")
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/đ/g, "d");
}

function escapeHtml(value) {
  return String(value || "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function imageBase(product) {
  const source = product.image || `${product.category}/${product.code}.png`;
  return String(source).replace(/\.(png|webp|jpg|jpeg)$/i, "");
}

